class RecyclerList {



    public String sala;
    public String horario;
    public String numeroDoData;
    public String equipamento;


    public RecyclerList(){

    }


    public RecyclerList(String sala, String equipamento, String numeroDoData, String horario){

        this.sala = sala;
        this.numeroDoData = numeroDoData;
        this.horario = horario;
        this.equipamento = equipamento;
    }



}



